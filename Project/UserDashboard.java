    private void btnsave2ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnsave2ActionPerformed
    {//GEN-HEADEREND:event_btnsave2ActionPerformed
       tblcustomer1.setEnabled(false);
        try {
        String productName = txtpname2.getText();
        String productId = null;

        DefaultTableModel tableModel = (DefaultTableModel) tblcart.getModel();
        boolean productExists = false;
        for (int i = 0; i < tableModel.getRowCount(); i++)
        {
            if (tableModel.getValueAt(i, 1).toString().equals(productName)) {
                productExists = true;
                break;
            }
        }

        if (productExists) {
            JOptionPane.showMessageDialog(ProductPanel, "Product is already in the cart!");
            return;
        }

        Connection con = CommonConnection.getConnection();
        String query = "Select ID from product where Name = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, productName);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            productId = rs.getString(1);
        }

        int r = tblproduct1.getSelectedRow();
        if (Double.parseDouble(tblproduct1.getValueAt(r, 3).toString()) < Double.parseDouble(txtquantity2.getText())) {
            JOptionPane.showMessageDialog(ProductPanel, "Required quantity is not available. Right now, Available Quantity = " + tblproduct1.getValueAt(r, 3).toString());
        } 
        else
        {
            String price = txtprice2.getText();
            String quantity = txtquantity2.getText();
            String description = txtdescription2.getText();
            String subTotal = String.valueOf(Double.parseDouble(quantity) * Double.parseDouble(price));
            String[] row = {productId, productName, price, quantity, description, subTotal};
            tableModel.addRow(row);
            totalAmount = 0.0;
            for (int i = 0; i < tableModel.getRowCount(); i++) 
            {
            double total = Double.parseDouble(tableModel.getValueAt(i, 5).toString());
            totalAmount += total;
            }
        txttotalprice.setText(String.valueOf(totalAmount));
//        txtcname2.setText(null);
//        txtemail2.setText(null);
//        txtmobile2.setText(null);
//        txtpname2.setText(null);
//        txtprice2.setText(null);
          txtquantity2.setText(null);
//        txtdescription2.setText(null);
        }

    } catch (SQLException ex) {
        Logger.getLogger(UserDashboard.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnsave2ActionPerformed

