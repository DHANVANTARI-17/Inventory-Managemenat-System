    private void tblcartMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_tblcartMouseClicked
    {//GEN-HEADEREND:event_tblcartMouseClicked
        int selectedRow = tblcart.getSelectedRow();
    
    if (selectedRow != -1) {  // Make sure a row is selected
        // Get the product name or ID from the selected row (assuming product name is in column 1)
        String productName = tblcart.getValueAt(selectedRow, 1).toString();
        
        // Prompt the user for confirmation
        int response = JOptionPane.showConfirmDialog(
            this, 
            "Do you want to delete the product \"" + productName + "\" from the cart?", 
            "Confirm Deletion", 
            JOptionPane.YES_NO_OPTION, 
            JOptionPane.QUESTION_MESSAGE
        );
        
        // If the user clicks "Yes", delete the selected row from the cart
        if (response == JOptionPane.YES_OPTION) {
            DefaultTableModel tableModel = (DefaultTableModel) tblcart.getModel();
            tableModel.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, productName + " has been removed from the cart.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a product to delete.");
    }
    }//GEN-LAST:event_tblcartMouseClicked

